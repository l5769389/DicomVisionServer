#!/usr/bin/env python3
from __future__ import annotations

import argparse
import asyncio
from dataclasses import dataclass, field
from statistics import mean, median
from time import perf_counter
from uuid import uuid4

import httpx
import socketio
from aiortc import RTCConfiguration, RTCIceServer, RTCPeerConnection, RTCSessionDescription


@dataclass
class BenchmarkSamples:
    frame_times: list[float] = field(default_factory=list)
    payload_sizes: list[int] = field(default_factory=list)

    def add(self, payload_size: int = 0) -> None:
        self.frame_times.append(perf_counter())
        self.payload_sizes.append(max(0, payload_size))


def percentile(values: list[float], ratio: float) -> float:
    if not values:
        return 0.0
    ordered = sorted(values)
    return ordered[min(len(ordered) - 1, max(0, round((len(ordered) - 1) * ratio)))]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Benchmark DicomVision 3D WebP or WebRTC transport.")
    parser.add_argument("--server", default="http://127.0.0.1:8100")
    parser.add_argument("--folder", required=True, help="DICOM folder path visible to the server")
    parser.add_argument("--transport", choices=("webp", "webrtc"), default="webrtc")
    parser.add_argument("--frames", type=int, default=12)
    parser.add_argument("--width", type=int, default=720)
    parser.add_argument("--height", type=int, default=720)
    parser.add_argument(
        "--interval-ms",
        type=float,
        default=0.0,
        help="Optional delay between renders; zero exercises continuous interaction",
    )
    parser.add_argument(
        "--settle-ms",
        type=float,
        default=250.0,
        help="Observe the media track after the final render to detect trailing frames",
    )
    return parser.parse_args()


async def wait_for_count(samples: BenchmarkSamples, count: int, timeout: float = 30.0) -> None:
    deadline = perf_counter() + timeout
    while len(samples.frame_times) < count:
        if perf_counter() >= deadline:
            raise TimeoutError(f"received {len(samples.frame_times)} of {count} frames")
        await asyncio.sleep(0.01)


async def wait_for_peer_connection(peer: RTCPeerConnection, timeout: float = 20.0) -> None:
    deadline = perf_counter() + timeout
    while peer.connectionState != "connected":
        if peer.connectionState in {"failed", "closed"}:
            raise RuntimeError(
                f"WebRTC connection failed: connection={peer.connectionState} "
                f"ice={peer.iceConnectionState}"
            )
        if perf_counter() >= deadline:
            raise TimeoutError(
                f"WebRTC connection timed out: connection={peer.connectionState} "
                f"ice={peer.iceConnectionState} gathering={peer.iceGatheringState}"
            )
        await asyncio.sleep(0.05)


async def run_benchmark(args: argparse.Namespace) -> None:
    # Match the compact workspace IDs generated by the clients. Some server
    # deployments cap workspace identifiers before registry lookup.
    workspace_id = f"bench-{uuid4().hex}"
    # Loading a large local folder can leave macOS' development HTTP stack
    # closing the keep-alive connection while the next request is queued.
    # REST setup is outside the measured media path, so use fresh connections.
    headers = {
        "X-DicomVision-Workspace-Id": workspace_id,
        "Connection": "close",
    }
    async with httpx.AsyncClient(base_url=args.server, headers=headers, timeout=120.0) as client:
        load_response = await client.post("/api/v1/dicom/loadFolder", json={"folderPath": args.folder})
        load_response.raise_for_status()
        series_list = [
            item
            for item in (load_response.json().get("seriesList") or [])
            if item.get("isImageSeries", True)
        ]
        if not series_list:
            raise RuntimeError("No DICOM image series was found")
        series = max(series_list, key=lambda item: int(item.get("instanceCount") or 0))
        create_response = await client.post(
            "/api/v1/view/create",
            json={"seriesId": series["seriesId"], "viewType": "3D"},
        )
        if create_response.is_error:
            raise RuntimeError(
                f"Failed to create 3D view ({create_response.status_code}): {create_response.text}"
            )
        view_id = create_response.json()["viewId"]
        size_response = await client.post(
            "/api/v1/view/setSize",
            json={
                "viewId": view_id,
                "opType": "setSize",
                "size": {"width": args.width, "height": args.height},
            },
        )
        size_response.raise_for_status()

        samples = BenchmarkSamples()
        socket = socketio.AsyncClient(reconnection=False)
        peer: RTCPeerConnection | None = None

        @socket.on("image_update")
        async def on_image_update(payload, image_binary, *_extra) -> None:
            if args.transport == "webp" and payload.get("viewId") == view_id:
                samples.add(len(image_binary))

        @socket.on("image_update_metadata")
        async def on_image_update_metadata(payload) -> None:
            if args.transport == "webrtc" and payload.get("viewId") == view_id:
                # Metadata is recorded separately from media receipt; the video
                # consumer below is the benchmark's frame-delivery clock.
                return

        await socket.connect(args.server, auth={"workspaceId": workspace_id}, socketio_path="socket.io")
        if args.transport == "webrtc":
            config = await socket.call("webrtc_3d_config", {})
            ice_servers = [
                RTCIceServer(
                    urls=item["urls"],
                    username=str(item.get("username") or ""),
                    credential=str(item.get("credential") or ""),
                )
                for item in config.get("iceServers", [])
            ]
            peer = RTCPeerConnection(RTCConfiguration(iceServers=ice_servers))
            peer.addTransceiver("video", direction="recvonly")

            @peer.on("track")
            def on_track(track) -> None:
                async def consume() -> None:
                    while True:
                        try:
                            await track.recv()
                        except Exception:
                            # Closing the peer ends the consumer with
                            # MediaStreamError; that is the normal benchmark
                            # shutdown path, not a transport failure.
                            return
                        samples.add()

                asyncio.create_task(consume())

            offer = await peer.createOffer()
            await peer.setLocalDescription(offer)
            answer = await socket.call(
                "webrtc_3d_offer",
                {"viewId": view_id, "sdp": peer.localDescription.sdp, "type": peer.localDescription.type},
                timeout=20,
            )
            if not answer.get("ok"):
                raise RuntimeError(answer.get("message") or "WebRTC offer failed")
            await peer.setRemoteDescription(RTCSessionDescription(sdp=answer["sdp"], type=answer["type"]))
            await wait_for_peer_connection(peer)

        warmup_target = 1
        await socket.call("bind_view", {"viewId": view_id, "render": True, "imageFormat": "webp"}, timeout=120)
        await wait_for_count(samples, warmup_target)
        if args.transport == "webrtc":
            await asyncio.sleep(0.15)
        samples.frame_times.clear()
        samples.payload_sizes.clear()

        started_at = perf_counter()
        request_latencies_ms: list[float] = []
        render_count = max(1, args.frames)
        for _ in range(render_count):
            first_frame_index = len(samples.frame_times)
            request_started_at = perf_counter()
            await socket.call("bind_view", {"viewId": view_id, "render": True, "imageFormat": "webp"}, timeout=120)
            await wait_for_count(samples, first_frame_index + 1, timeout=120)
            request_latencies_ms.append(
                (samples.frame_times[first_frame_index] - request_started_at) * 1000.0
            )
            if args.interval_ms > 0:
                await asyncio.sleep(args.interval_ms / 1000.0)
        elapsed_ms = (perf_counter() - started_at) * 1000.0
        delivered_at_completion = len(samples.frame_times)
        if args.settle_ms > 0:
            await asyncio.sleep(args.settle_ms / 1000.0)
        trailing_frames = len(samples.frame_times) - delivered_at_completion

        intervals_ms = [
            (current - previous) * 1000.0
            for previous, current in zip(samples.frame_times, samples.frame_times[1:])
        ]
        nonzero_payloads = [size for size in samples.payload_sizes if size]
        print(
            f"transport={args.transport} renders={render_count} "
            f"media_frames={delivered_at_completion} trailing_media_frames={trailing_frames} "
            f"total_ms={elapsed_ms:.1f}"
        )
        print(
            "render_to_frame_ms "
            f"mean={mean(request_latencies_ms):.1f} "
            f"p50={median(request_latencies_ms):.1f} "
            f"p95={percentile(request_latencies_ms, 0.95):.1f}"
        )
        print(
            "frame_interval_ms "
            f"mean={mean(intervals_ms) if intervals_ms else 0.0:.1f} "
            f"p50={median(intervals_ms) if intervals_ms else 0.0:.1f} "
            f"p95={percentile(intervals_ms, 0.95):.1f}"
        )
        print(
            "payload_bytes "
            f"mean={mean(nonzero_payloads) if nonzero_payloads else 0.0:.0f} "
            f"total={sum(nonzero_payloads)}"
        )

        if peer is not None:
            await socket.call("webrtc_3d_close", {"viewId": view_id})
            await peer.close()
        await socket.disconnect()
        await client.post("/api/v1/view/close", json={"viewId": view_id})


if __name__ == "__main__":
    asyncio.run(run_benchmark(parse_args()))
